def solve(s):
    cs = [int(c) for c in s]
    cs.append(cs[0])

    sum = 0
    for idx in range(len(cs) - 1):
        if cs[idx] == cs[idx + 1]:
            sum += cs[idx]

    return sum


def solve_half(s):
    step = int(len(s) / 2)
    cs = [int(c) for c in s]
    cs.extend(cs)

    sum = 0
    for idx in range(len(s)):
        if cs[idx] == cs[idx + step]:
            sum += cs[idx]

    return sum


